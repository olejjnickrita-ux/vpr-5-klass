const taskBadge = document.getElementById("taskBadge");
const question = document.getElementById("question");
const textContainer = document.getElementById("textContainer");
const showCardBtn = document.getElementById("showCardBtn");
const resultCard = document.getElementById("resultCard");
const taskScreen = document.getElementById("taskScreen");
const finishScreen = document.getElementById("finishScreen");
const answerWord = document.getElementById("answerWord");
const answerText = document.getElementById("answerText");
const answerImage = document.getElementById("answerImage");
const imageWrap = document.getElementById("imageWrap");
const nextTaskBtn = document.getElementById("nextTaskBtn");
const restartBtn = document.getElementById("restartBtn");
const progressText = document.getElementById("progressText");
const progressBar = document.getElementById("progressBar");

let currentTaskIndex = 0;
let cardWasShown = false;

function normalizeWord(value) {
  return value
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[.,!?;:()«»"—…]/g, "")
    .trim();
}

function renderTask() {
  const task = tasks[currentTaskIndex];
  cardWasShown = false;

  taskBadge.textContent = `Задание ${currentTaskIndex + 1}`;
  question.textContent = task.prompt;
  resultCard.classList.add("hidden");
  finishScreen.classList.add("hidden");
  taskScreen.classList.remove("hidden");

  progressText.textContent = `${currentTaskIndex + 1} / ${tasks.length}`;
  progressBar.style.width = `${((currentTaskIndex + 1) / tasks.length) * 100}%`;

  const words = task.text.split(/(\s+)/);

  textContainer.innerHTML = "";
  words.forEach((part) => {
    if (/^\s+$/.test(part)) {
      textContainer.appendChild(document.createTextNode(part));
      return;
    }

    const button = document.createElement("span");
    button.className = "word";
    button.textContent = part;
    button.dataset.normalized = normalizeWord(part);
    button.addEventListener("click", () => handleWordClick(button));
    textContainer.appendChild(button);
  });
}

function handleWordClick(wordEl) {
  if (cardWasShown) return;

  const task = tasks[currentTaskIndex];
  const clicked = wordEl.dataset.normalized;
  const correct = normalizeWord(task.answer);

  if (clicked === correct) {
    wordEl.classList.remove("wrong");
    wordEl.classList.add("correct");
    openCard(true);
  } else {
    wordEl.classList.add("wrong");
  }
}

function revealCorrectWord() {
  const task = tasks[currentTaskIndex];
  const correct = normalizeWord(task.answer);
  const allWords = textContainer.querySelectorAll(".word");

  allWords.forEach((word) => {
    if (word.dataset.normalized === correct) {
      word.classList.add("revealed");
    }
  });
}

function openCard(foundCorrectly) {
  const task = tasks[currentTaskIndex];
  cardWasShown = true;

  if (!foundCorrectly) {
    revealCorrectWord();
  }

  const visibleWord = task.answer.replace(/[.,!?;:()«»"—…]/g, "");
  answerWord.textContent = task.cardTitle || visibleWord;
  answerText.textContent = task.cardText;

  if (task.image) {
    answerImage.src = task.image;
    answerImage.alt = task.cardTitle || visibleWord;
    imageWrap.classList.remove("hidden");
  } else {
    answerImage.src = "";
    answerImage.alt = "";
    imageWrap.classList.add("hidden");
  }

  taskScreen.classList.add("hidden");
  resultCard.classList.remove("hidden");
}

showCardBtn.addEventListener("click", () => {
  openCard(false);
});

nextTaskBtn.addEventListener("click", () => {
  currentTaskIndex += 1;

  if (currentTaskIndex >= tasks.length) {
    resultCard.classList.add("hidden");
    taskScreen.classList.add("hidden");
    finishScreen.classList.remove("hidden");
    progressText.textContent = `${tasks.length} / ${tasks.length}`;
    progressBar.style.width = "100%";
    return;
  }

  renderTask();
});

restartBtn.addEventListener("click", () => {
  currentTaskIndex = 0;
  renderTask();
});

renderTask();